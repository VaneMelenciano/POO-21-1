    private void jButton_AgregarMecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AgregarMecaActionPerformed
        if(evt.getSource()==jButton_AgregarMeca){ //pregunta si el boton es el que causa la accion
            String RFC = jTextPane_RFC.getText();
            String nom = jTextPane_nombre.getText();
            String dep = jTextPane_departamento.getText();
            String pue = jTextPane_puesto.getText();
            float sueldo = Float.parseFloat(jTextPane_Trabajos.getText());
            me = new Mecanico(RFC, nom, dep, pue);
            me.setTrabajos(sueldo);
            ventana_mostrar vm = new ventana_mostrar(me);
            vm.setVisible(true);
        }
    }//GEN-LAST:event_jButton_AgregarMecaActionPerformed

